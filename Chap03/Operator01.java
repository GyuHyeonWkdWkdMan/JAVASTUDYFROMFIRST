package Chap03;

public class Operator01 {
    public static void main(String[] args) {
        int x = 10;
        double y = 2.0;
        System.out.println("x + y = " + (x + y));
        System.out.println("x - y = " + (x - y));
        System.out.println("x * y = " + (x * y));
        System.out.println("x / y = " + (x / y));
        System.out.println("x % y = " + (x % y));
    }
}
